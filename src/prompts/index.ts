import type { DetailSection } from "../types";

// ─── All AI prompts in one place ─────────────────────────────────────────────
// Keeping prompts centralised makes iteration faster and keeps component files lean.
//
// 设计原则（去字数化版本）：
//   不再用字数控制输出长度。原因：AI 边写边数字会硬切句末造成截断。
//   改为用「结构 + 质量要素」约束：
//   - 每节明确"必须包含的信息要素"（如易错警告必须有"现象+原因+避免"）
//   - 列表类明确"几条"，但不限单条长度
//   - 整体强调"完整成句，不要为长度而截断"
//   - 末尾加自检指令

// ── Unified detail page (记忆详情页 — DeepSeek 生成全部可见内容) ──────────────
// 接收豆包视觉模型的原始提取结果作为 context，输出五个结构化模块。
// 所有用户可见文字均来自此函数，豆包结果只作为中间数据。
export function buildDetailPagePrompt(params: {
  skill: string;
  title: string;
  hasAnnotations: boolean;
  overview: string;
  detailIntro: string;
  detailSections: DetailSection[];
  aiKeyPoints: string[];
}): string {
  const { skill, title, hasAnnotations, overview, detailIntro, detailSections, aiKeyPoints } = params;

  const contextLines: string[] = [
    `卡片标题：${title}`,
    `分析模式：${hasAnnotations ? "用户已圈选重点区域" : "全图分析（无圈选）"}`,
    `图片概述：${overview || "(无)"}`,
  ];
  if (detailIntro) contextLines.push(`重点定位：${detailIntro}`);
  if (detailSections.length > 0) {
    contextLines.push("已提取重点内容：");
    detailSections.forEach(s => {
      contextLines.push(`  · ${s.title}：${s.items.join("；")}`);
    });
  }
  if (aiKeyPoints.length > 0) {
    contextLines.push(`核心关键词：${aiKeyPoints.join("、")}`);
  }
  const context = contextLines.join("\n");

  const [sec2Label, sec2Instructions, sec3Label, sec3Instructions] = getSkillSections(skill);

  return `你是一位有丰富大学教学经验的学长/学姐。以下是AI从学生学习图片中自动提取的原始内容（不要逐字复述，要升华、深化、丰富细节）：

===原始提取内容===
${context}
=================

请基于以上内容，输出一份完整的"记忆详情"分析报告，严格按以下五个模块顺序输出，每个模块以【模块名】单独占一行作为标题，模块之间空一行；不使用任何 markdown 符号（#、*、-、**、> 等），直接换行分段。

【蓝色链接规则】
- 五个模块每个都必须自然出现 1~2 个 \`[[学术名词]]\`，前端会渲染为蓝色可点击链接，知识关联模块需要 2 个以上。
- 中括号里只放名词本身，不带动词/形容词/标点；每个 \`[[\` 必须有配对的 \`]]\`。
- 不要包裹"当前卡片主题词自身"，要包裹相邻/对照/扩展的其他学术概念。

【内容概述】
用流畅自然的语言概括本次学习内容的主题、背景和核心价值，帮同学建立全局视角。2-3 句完整的话，每句以中文句号收尾。

【${sec2Label}】
${sec2Instructions}

【${sec3Label}】
${sec3Instructions}

【知识关联】
列出 2-3 个与本内容密切关联的周边学术概念，每条以 \`[[概念名]]\` 开头，格式：[[概念名]] → 一句完整的话说明为什么关联、学习时要结合哪里，以句号结尾。
✓ 示例：[[动量守恒定律]] → 与本节的冲量推导直接互补，做综合大题时往往需要两个定律联立求解。

【学习建议】
给出 1-2 条具体可执行的学习建议，落到"看哪本教材哪一章 / 做哪类题 / 结合哪个知识点复习"，每条以句号结尾。

══════════════════ 强制约束（违反即视为错误输出） ══════════════════
1. 每句话都以中文句号「。」收尾，严禁停在「、」「，」「；」上。
2. 每个模块独立完整，不要依赖被截断的"待续"语句。
3. 除"知识关联"已经强制至少 2 个外，其余模块每个至少 1 个 [[名词]]；所有 [[ 必须有配对的 ]]。
4. 不要包裹当前卡片主题词自身。
5. 【公式排版】凡涉及公式、符号、单位、下标上标，一律用 LaTeX：行内公式用单美元符号包裹（如 $E=mc^2$、$F=ma$）；重要公式或独立成行的等式用双美元符号单独占一行（如 $$\\int_0^1 f(x)\\,dx$$）。不要用 Unicode 拼凑复杂公式，普通中文叙述仍用纯文本。
5. 语气像学长辅导学弟妹，亲切自然，用"你"。`;
}

function getSkillSections(skill: string): [string, string, string, string] {
  switch (skill) {
    case "math_problem":
      return [
        "解题思路",
        `分析这道题/这个知识点的核心解题思路：①题型识别特征 → ②核心方法选择依据 → ③关键转化步骤。每步是完整一句话，至少说明"用了什么 + 为什么用 + 关键操作"，以句号结尾。`,
        "易错陷阱",
        `列出 1-2 条最容易在这类题上出错的地方，每条用"⚠ "开头。每条必须包含：①哪一步容易出错 → ②出错原因 → ③应对策略，整条以句号结尾。`,
      ];
    case "language":
      return [
        "语言要点",
        `提炼本次学习的核心词汇或语法结构，给出精准解释，并与中文进行对比，点明母语干扰点。每个要点是完整一句话，以句号结尾。`,
        "地道用法",
        `给出 1-2 个地道例句，每句后附中文翻译和语境说明（学术写作 / 日常 / 考试），要展示真实语境而非教材腔，以句号结尾。`,
      ];
    case "experiment_lab":
      return [
        "实验要点",
        `概括实验目的、核心假设和关键步骤（控制变量、主要操作），点明"为什么这样做"，每步是完整一句话，以句号结尾。`,
        "误差与注意",
        `列出 1-2 条主要误差来源或注意事项，每条用"⚠ "开头，包含：①误差来源 → ②如何影响结果 → ③控制方法，以句号结尾。`,
      ];
    case "code_cs":
      return [
        "核心思路",
        `说明这段代码/算法的设计思路（解决什么问题 + 用什么核心思想），并给出时间/空间复杂度及推导依据。每句完整，以句号结尾。`,
        "常见Bug与边界",
        `列出 1-2 条典型 Bug 或边界情况，每条用"⚠ "开头，包含：①Bug 现象 → ②原因 → ③防范方法，以句号结尾。`,
      ];
    case "literature_essay":
      return [
        "核心论点",
        `提炼核心论点或主旨观点，分析作者的论证方式（归纳/演绎/类比/案例），评估论据充分性，每句完整，以句号结尾。`,
        "批判性视角",
        `从批判性角度提出 1-2 个深思问题：论证前提是否成立？有无反例？视角是否片面？每个问题要具体到内容细节，以句号结尾。`,
      ];
    default: // theory_concept
      return [
        "核心原理",
        `列出 2-3 条核心原理或规律，每条以序号（1. 2. 3.）开头，包含"规律名称 + 具体说明 + 关键公式或适用条件"，不少于 25 字，以句号结尾。`,
        "考点解析",
        `说明这个知识点最常以什么形式出现在大学考试中，给出 1-2 条具体答题思路（看到什么特征 → 用什么方法 → 关键步骤），每条以句号结尾。`,
      ];
  }
}

